#Operadores matematicos
# Suma
print(2 + 3)  # Resultado: 5
# Resta
print(5 - 2)  # Resultado: 3
# Multiplicación
print(2 * 3)  # Resultado: 6         
# División
print(6 / 2)  # Resultado: 3.0
# División entera
print(7 // 2)  # Resultado: 3   
# Módulo (resto de la división)
print(7 % 2)  # Resultado: 1
# Potenciación  
print(2 ** 3)  # Resultado: 8

# Operadores de comparación
# Igualdad  
print(2 == 2)  # Resultado: True
# Desigualdad
print(2 != 3)  # Resultado: True
# Mayor que
print(3 > 2)  # Resultado: True
# Menor que 
print(2 < 3)  # Resultado: True
# Mayor o igual que
print(3 >= 3)  # Resultado: True
# Menor o igual que
print(2 <= 3)  # Resultado: True

# Operadores lógicos
# AND (y lógico)    
print(True and False)  # Resultado: False
# OR (o lógico)
print(True or False)  # Resultado: True
# NOT (negación lógica)
print(not True)  # Resultado: False
# Operadores de asignación
# Asignación simple
x = 5
print(x)  # Resultado: 5
# Asignación con suma
x += 2  # Equivalente a x = x + 2